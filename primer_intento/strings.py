mystr = " Hello World "

#dir ensena que se puede hacer con ese tipo de dato
#print (dir(mystr)) 
# el metodo tiene que ir con parentesis
print(mystr.upper())  # imprime mayusculas
print(mystr.lower())  # imprime minusculas
print(mystr.swapcase()) # cambia de mayusculas a minusculas todo el texto
print(mystr.capitalize())# cambia de mayuscula la primera letra
print(mystr.replace("Hello", "bye")) #reemplaza texto
print(mystr.count("l")) #cuenta el numero de veces que esta la letra L